package in.com.Basic;

public class UserPrime {

	public static void main(String[] args) {
		int num=11;
		int count=0;
		for (int i = 2; i < num; i++) {
			if(num%i==0) {
				count=1;
			}
			
		}
		if(count==0){
			System.out.println("prime number");
		}
		else {
			System.out.println("not prime");
		}

	}

}
