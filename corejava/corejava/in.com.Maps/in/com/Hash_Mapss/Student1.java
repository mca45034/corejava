package in.com.Hash_Mapss;

public class Student1 {
	int id;
	String name;
	String location;
	public Student1(int id, String name, String location) {
		super();
		this.id = id;
		this.name = name;
		this.location = location;
	}
	@Override
	public String toString() {
		return "Student1 [id=" + id + ", name=" + name + ", location=" + location + "]";
	}
	

}
