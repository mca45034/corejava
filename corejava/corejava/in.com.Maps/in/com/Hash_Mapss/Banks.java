package in.com.Hash_Mapss;

public class Banks {
	String name;
	String location;
	public Banks(String name, String location) {
		super();
		this.name = name;
		this.location = location;
	}
	@Override
	public String toString() {
		return "Banks [name=" + name + ", location=" + location + "]";
	}
	
	

}
