package in.com.Incapsulation12;

public class Shape {
	protected String color;
	protected int border;
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public int getBorder() {
		return border;
	}
	public void setBorder(int border) {
		this.border = border;
	}
	

}
