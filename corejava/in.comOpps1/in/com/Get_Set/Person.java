package in.com.Get_Set;

public class Person {
	String name;
	int dob;
	String address;
	public void setName(String name) {
		name=name;
	}
	public String getName() {
		return name;
	}
	public void setDob (int dob) {
		dob=dob;
	}
	public int getDob() {
		return dob;
	}
	public void setAddress (String address) {
		this.address=address;
	}
	public String getAddress() {
		return address;
	}
	

}
