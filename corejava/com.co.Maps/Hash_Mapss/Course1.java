package Hash_Mapss;

public class Course1 {
	int cid;
	String cName;
	int price;
	public Course1(int cid, String cName, int price) {
		super();
		this.cid = cid;
		this.cName = cName;
		this.price = price;
	}
	@Override
	public String toString() {
		return "Course1 [cid=" + cid + ", cName=" + cName + ", price=" + price + "]";
	}
	

}
